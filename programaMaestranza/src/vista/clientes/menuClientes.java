    public void valirCamposVacios(String dato, String campo) throws Exception {
        if (dato.trim().length() == 0) {
            throw new Exception("El campo " + campo + " no debe venir Vacio.");
        }
    }
    private void btn_AgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AgregarClienteActionPerformed
        // TODO add your handling code here:
        try {
            this.valirCamposVacios(this.txt_rutCliente.getText(), "RUT");
            this.valirCamposVacios(this.txt_nombreEmpresaCliente.getText(), "NOMBRE EMPRESA");
            this.valirCamposVacios(this.txt_giroCliente.getText(), "GIRO EMPRESA");

            String rut = this.txt_rutCliente.getText().toUpperCase().replace(".", "");
            String nombreEmpresa = this.txt_nombreEmpresaCliente.getText();
            String giro = this.txt_giroCliente.getText();
            String direccion = this.txt_direccion.getText();
            if (direccion.isEmpty()) {
                direccion = "";
            }
            String nombreContacto = this.txt_nombreContacto.getText();
            if (nombreContacto.isEmpty()) {
                nombreContacto = "";
            }
            String numeroContacto = this.txt_numeroContacto.getText();
            if (numeroContacto.isEmpty()) {
                numeroContacto = "";
            }

            String telefono = this.txt_numeroTelefonoEmpresa.getText();
            if (telefono.isEmpty()) {
                telefono = "";
            }

            ClienteDao clientex = new ClienteDao();
            if (clientex.consultarSiExisteRut(rut)) {
                JOptionPane.showMessageDialog(null, "Este rut ya Existe");
            } else {
                if (clientex.insertarCliente(nombreEmpresa, rut, giro, direccion, nombreContacto, numeroContacto, telefono)) {
                    JOptionPane.showMessageDialog(null, "SE AGREGO CORRECTAMENTE");
                    this.limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "NO SE PUDO AGREGAR");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_btn_AgregarClienteActionPerformed

    private void txt_rutClienteCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_rutClienteCaretUpdate
        // Si el rut existe rellenar datos, sino no rellenar
        try {
            ClienteDao clientex = new ClienteDao();
            String rut = vista.clientes.menuClientes.txt_rutCliente.getText().trim().replace(".", "").toUpperCase();
            if (clientex.consultarSiExisteRut(rut)) {
                //Cargo valores
                Cliente cliente = clientex.rescatarConsultaCliente(rut);

                this.txt_nombreEmpresaCliente.setText(cliente.getNombre());
                this.txt_giroCliente.setText(cliente.getGiro());
                this.txt_direccion.setText(cliente.getDireccion());
                this.txt_numeroTelefonoEmpresa.setText(cliente.getTelefono());
                this.txt_nombreContacto.setText(cliente.getNombreContacto());
                this.txt_numeroContacto.setText(cliente.getNumeroContacto());

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_txt_rutClienteCaretUpdate

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        try {
            this.valirCamposVacios(vista.clientes.menuClientes.txt_rutCliente.getText(), "RUT");

            String rut = vista.clientes.menuClientes.txt_rutCliente.getText().trim().replace(".", "").toUpperCase();

            ClienteDao clientex = new ClienteDao();
            int confirmacion = JOptionPane.YES_NO_OPTION;
            //System.out.println("Numero Confirmacion ELiminacion: "+ confirmacion);
            int numeroConfirmacion = JOptionPane.showConfirmDialog(null, "¿Desea Eliminarlo Realmente?", "Warning", confirmacion);

            if (numeroConfirmacion == 0) {
                if (clientex.eliminarCliente(rut)) {
                    JOptionPane.showMessageDialog(null, "Eliminado Correctamente");
                    this.limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo Eliminar");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_modificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarClienteActionPerformed
        try {
            this.valirCamposVacios(vista.clientes.menuClientes.txt_rutCliente.getText().toString(), "RUT");
            String rut = vista.clientes.menuClientes.txt_rutCliente.getText().toString().replace(".", "").toUpperCase();
            ClienteDao clientex = new ClienteDao();
            
            if(clientex.consultarSiExisteRut(rut)){
                /*Modificar Datos*/
                String nombre = this.txt_nombreEmpresaCliente.getText().trim();
                String giro = this.txt_giroCliente.getText().trim();
                String direccion = this.txt_direccion.getText().trim();
                String nroTele = this.txt_numeroTelefonoEmpresa.getText().trim();
                String nombreContacto = this.txt_nombreContacto.getText().trim();
                String numeroContacto = this.txt_numeroContacto.getText().trim();
                
                if (clientex.modificarCliente(nombre,rut,giro,direccion,nombreContacto,numeroContacto,nroTele)) {
                    JOptionPane.showMessageDialog(null, "Modificado Correctamente");
                }else{
                    JOptionPane.showMessageDialog(null, "No se modifico");
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "No se pudo modificar");
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_btn_modificarClienteActionPerformed

