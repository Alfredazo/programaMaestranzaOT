    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            if (this.tablaProductos.getRowCount() > 0) {
                this.eliminarContenidoTabla();
            }
            this.cargarTablaInicio();
            this.txt_nombreProducto.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (this.txt_nombreProducto.getText().isEmpty()) {
                throw new Exception("Para utilizar esta funciÃ³n, ingrese al menos un caracter");
            }

            String nombreBuscar = this.txt_nombreProducto.getText();
            if (this.tablaProductos.getRowCount() > 0) {
                this.eliminarContenidoTabla();
            }
            this.cargarTablaInicio(nombreBuscar);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
