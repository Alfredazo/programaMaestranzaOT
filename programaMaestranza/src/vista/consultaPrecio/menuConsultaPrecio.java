    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaProductos.getModel();
        int a = this.tablaProductos.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void cargarTablaInicio() {
        ProductoExportDao producto = new ProductoExportDao();
        ArrayList<ProductoExport> listado = producto.listadoProductos();
        DefaultTableModel modelo = (DefaultTableModel) this.tablaProductos.getModel();

        Object[] fila = new Object[modelo.getColumnCount()];
        for (int i = 0; i < listado.size(); i++) {
            fila[0] = listado.get(i).getIdProducto();
            fila[1] = listado.get(i).getNombreProducto();
            fila[2] = listado.get(i).getNombreEmpresa();
            fila[3] = listado.get(i).getPrecioMinimo();
            fila[4] = listado.get(i).getPrecioMaximo();

            modelo.addRow(fila);
        }
    }

    public void cargarTablaInicio(String nombreBuscar) {
        ProductoExportDao producto = new ProductoExportDao();
        ArrayList<ProductoExport> listado = producto.listadoProductos(nombreBuscar.toUpperCase());
        DefaultTableModel modelo = (DefaultTableModel) this.tablaProductos.getModel();

        Object[] fila = new Object[modelo.getColumnCount()];
        for (int i = 0; i < listado.size(); i++) {
            fila[0] = listado.get(i).getIdProducto();
            fila[1] = listado.get(i).getNombreProducto();
            fila[2] = listado.get(i).getNombreEmpresa();
            fila[3] = listado.get(i).getPrecioMinimo();
            fila[4] = listado.get(i).getPrecioMaximo();

            modelo.addRow(fila);
        }
    }

