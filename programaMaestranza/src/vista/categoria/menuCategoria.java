    private void tablaCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCategoriaMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            //Enviar datos a panel
            int idTipo = Integer.parseInt(this.tablaCategoria.getValueAt(this.tablaCategoria.getSelectedRow(), 0).toString());
            CategoriaDao categoriax = new CategoriaDao();
            Categoria categoria = categoriax.obtenerDatosCategoria(idTipo);

            this.txt_numeroCategoria.setText(String.valueOf(idTipo));
            this.txt_nombreCategoria.setText(categoria.getNombreCategoria());
            this.txt_descripccion.setText(categoria.getDescripcion());
        }
    }//GEN-LAST:event_tablaCategoriaMouseClicked

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        try {
            CategoriaDao categoriax = new CategoriaDao();
            int idTipo = Integer.parseInt(this.txt_numeroCategoria.getText());
            if (categoriax.validarSiExiste(idTipo)) {
                //Modifico -
                this.validarCamposVacio(this.txt_nombreCategoria.getText(), "NOMBRE CATEGORIA");
                this.validarCamposVacio(this.txt_descripccion.getText(), "DESCRIPCION");
                String nombreCategoria = this.txt_nombreCategoria.getText();
                String descripcion = this.txt_descripccion.getText();
                if (categoriax.modificarCategoria(idTipo, nombreCategoria, descripcion)) {
                    JOptionPane.showMessageDialog(null, "Se modifico Correctmente");
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo modificar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Primero cree la     categoria");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_btn_modificarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        //validar si existe
        CategoriaDao categoriax = new CategoriaDao();
        int idtipoCategoria = Integer.parseInt(this.txt_numeroCategoria.getText());

        if (categoriax.validarSiExiste(idtipoCategoria)) {
            //Consultar si lo elimino
            int confirmacion = JOptionPane.YES_NO_OPTION;
            //System.out.println("Numero Confirmacion ELiminacion: "+ confirmacion);
            int numeroConfirmacion = JOptionPane.showConfirmDialog(null, "¿Desea Eliminarlo Realmente?", "Warning", confirmacion);

            if (numeroConfirmacion == 0) {
                //Eliminar
                if (categoriax.eliminarCategoria(idtipoCategoria)) {
                    JOptionPane.showMessageDialog(null, "Se elimino correctamente");
                    this.limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "No existe ese ID");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "No existe ese ID de Categoria");
        }


    }//GEN-LAST:event_btn_eliminarActionPerformed

