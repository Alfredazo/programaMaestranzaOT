    public void validarCamposVacio(String dato, String campo) throws Exception {
        if (dato.trim().length() == 0) {
            throw new Exception("El campo " + campo + " no puede venir vacio");
        }
    }

    public void limpiarCampos() {
        this.txt_nombreCategoria.setText("");
        this.txt_descripccion.setText("");
        this.txt_nombreBuscar.setText("");
        this.rescatarIDCategoria();
    }

    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        /*Agregar Categoria*/
        try {
            this.validarCamposVacio(this.txt_nombreCategoria.getText(), "NOMBRE CATEGORIA");
            this.validarCamposVacio(this.txt_descripccion.getText(), "DESCRPCION");

            int idCategoria = Integer.parseInt(this.txt_numeroCategoria.getText());
            String nombreCategoria = this.txt_nombreCategoria.getText();
            String descripccion = this.txt_descripccion.getText();

            CategoriaDao categoriax = new CategoriaDao();
            /*Validar si existe en la bd*/
            if (!categoriax.validarSiExiste(idCategoria)) {
                //Agregar y llamar metodo insertar categoria
                if (categoriax.insertarCategoria(nombreCategoria, descripccion)) {
                    JOptionPane.showMessageDialog(null, "Se agrego Correctamente");
                    this.limpiarCampos();

                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo agregar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ya Existe esta categoria");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_btn_agregarActionPerformed

