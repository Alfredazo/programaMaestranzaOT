    public void rescatarIDCategoria() {
        CategoriaDao categoriax = new CategoriaDao();
        int id = categoriax.obtenerIDCategoria() + 1;
        this.txt_numeroCategoria.setText(String.valueOf(id));
    }

    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaCategoria.getModel();
        int a = this.tablaCategoria.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void cargarTablaInicio() {

        if (this.tablaCategoria.getSelectedRow() != 0) {
            this.eliminarContenidoTabla();
        }
        CategoriaDao categoriax = new CategoriaDao();
        ArrayList<Categoria> listado = categoriax.obtenerListadoCategoria();
        DefaultTableModel modelo = (DefaultTableModel) this.tablaCategoria.getModel();

        Object[] fila = new Object[modelo.getColumnCount()];
        for (int i = 0; i < listado.size(); i++) {
            fila[0] = listado.get(i).getId();
            fila[1] = listado.get(i).getNombreCategoria();
            fila[2] = listado.get(i).getDescripcion();

            modelo.addRow(fila);
        }
    }

    public void cargarTablaInicioPornombre(String nombreBuscar) {

        if (this.tablaCategoria.getSelectedRow() != 0) {
            this.eliminarContenidoTabla();
        }
        CategoriaDao categoriax = new CategoriaDao();
        ArrayList<Categoria> listado = categoriax.obtenerListadoCategoriaPorNombre(nombreBuscar);
        DefaultTableModel modelo = (DefaultTableModel) this.tablaCategoria.getModel();

        Object[] fila = new Object[modelo.getColumnCount()];
        for (int i = 0; i < listado.size(); i++) {
            fila[0] = listado.get(i).getId();
            fila[1] = listado.get(i).getNombreCategoria();
            fila[2] = listado.get(i).getDescripcion();

            modelo.addRow(fila);
        }
    }

