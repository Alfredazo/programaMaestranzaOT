    public void llenarComboBoxGiroProveedores() {
        try {
            ClienteDao clientex = new ClienteDao();
            ArrayList<String> listado = clientex.cargarComboBoxGiro();

            this.cboGiroEmpresa.removeAllItems();

            for (int i = -1; i < listado.size(); i++) {
                if (i == -1) {
                    this.cboGiroEmpresa.addItem("Seleccionar");
                } else {
                    this.cboGiroEmpresa.addItem(listado.get(i));
                }
            }
            /*Fin llenado*/
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema CBO: " + e.getMessage());
        }
    }

    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaClientes.getModel();
        int a = this.tablaClientes.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void cargarTablaInicio() {
        try {
            ClienteDao clientex = new ClienteDao();

            DefaultTableModel modelo = (DefaultTableModel) this.tablaClientes.getModel();
            ArrayList<Cliente> listadoClientes = clientex.rescatarConsultaProveedores();

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listadoClientes.size(); i++) {
                fila[0] = listadoClientes.get(i).getRut();
                fila[1] = listadoClientes.get(i).getNombre();
                fila[2] = listadoClientes.get(i).getGiro();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }

    public void cargarTablaListadoPorFiltro(String giro) {
        //Listar empresa
        try {
            //Vaciar Campos de la tabla
            eliminarContenidoTabla();

            ClienteDao clientex = new ClienteDao();

            DefaultTableModel modelo = (DefaultTableModel) this.tablaClientes.getModel();
            ArrayList<Cliente> listadoClientes = clientex.rescatarConsultaProveedores(giro);

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listadoClientes.size(); i++) {
                fila[0] = listadoClientes.get(i).getRut();
                fila[1] = listadoClientes.get(i).getNombre();
                fila[2] = listadoClientes.get(i).getGiro();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }

