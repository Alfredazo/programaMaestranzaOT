    private void tablaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMouseClicked
        if (evt.getClickCount() == 2) {
            //Rescatar valores y pasar
            String nombre = this.tablaClientes.getValueAt(this.tablaClientes.getSelectedRow(), 1).toString();
            vista.ordenTrabajo.menuOT.txt_nombreCliente.setText(nombre);
            this.dispose();
        }
    }//GEN-LAST:event_tablaClientesMouseClicked

    private void cboGiroEmpresaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboGiroEmpresaItemStateChanged
        /*Rescatar selecccionado y listar tabla*/
        try {
            if (this.cboGiroEmpresa.getSelectedItem().equals("Seleccionar")) {
                this.eliminarContenidoTabla();
                this.cargarTablaInicio();
            } else {
                this.cargarTablaListadoPorFiltro(this.cboGiroEmpresa.getSelectedItem().toString());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_cboGiroEmpresaItemStateChanged
