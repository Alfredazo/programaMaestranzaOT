    public void cargarUltimoIDFactura() {
        if (vista.ordenTrabajo.menuOT.txt_facturasAsignadas.getText().equals("S/A")) {
            //No asignada por lo tanto es una nueva OT
            FacturaDao facturax = new FacturaDao();
            int idFactura = facturax.obtenerUltimoIDFactura() + 1;
            this.txt_idFactura.setText(String.valueOf(idFactura));
        } else {
            this.txt_idFactura.setText(vista.ordenTrabajo.menuOT.txt_facturasAsignadas.getText());
        }
    }
    
