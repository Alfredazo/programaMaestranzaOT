    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            //Validar que no exista y agregar   
            FacturaDao facturax = new FacturaDao();
            int idFactura = Integer.parseInt(this.txt_idFactura.getText());
            if (facturax.validarExistaFactura(idFactura)) {
                //Rescatar datos
                Factura factura = facturax.obtenerFacturaPorID(idFactura);
                this.txt_factura1.setText(String.valueOf(factura.getFactura1()));
                this.txt_factura2.setText(String.valueOf(factura.getFactura2()));
                this.txt_factura3.setText(String.valueOf(factura.getFactura3()));
                this.txt_factura4.setText(String.valueOf(factura.getFactura4()));
                this.txt_factura5.setText(String.valueOf(factura.getFactura5()));
                JOptionPane.showMessageDialog(null, "Datos cargados, ya existe este ID");
            } else {
                //Agregar nuevo
                if (this.txt_factura1.getText().isEmpty()) {
                    throw new Exception("Ingrese al menos una factura, sino utilizar opcion agregar y cerrar.");
                }
                int factura1 = Integer.parseInt(this.txt_factura1.getText());

                int factura2 = 0;
                if (!this.txt_factura2.getText().isEmpty()) {
                    factura2 = Integer.parseInt(this.txt_factura2.getText());
                }

                int factura3 = 0;
                if (!this.txt_factura3.getText().isEmpty()) {
                    factura3 = Integer.parseInt(this.txt_factura3.getText());
                }

                int factura4 = 0;
                if (!this.txt_factura4.getText().isEmpty()) {
                    factura4 = Integer.parseInt(this.txt_factura4.getText());
                }

                int factura5 = 0;
                if (!this.txt_factura2.getText().isEmpty()) {
                    factura5 = Integer.parseInt(this.txt_factura2.getText());
                }

                if (facturax.insertarFactura(factura1, factura2, factura3, factura4, factura5)) {
                    JOptionPane.showMessageDialog(null, "Agregado Correctamente");
                    this.dispose();
                    vista.ordenTrabajo.menuOT.txt_facturasAsignadas.setText(this.txt_idFactura.getText());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo agregar");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnAgregarYCerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarYCerrarActionPerformed
        try {
            int confirmacion = JOptionPane.YES_NO_OPTION;
            //System.out.println("Numero Confirmacion ELiminacion: "+ confirmacion);
            int numeroConfirmacion = JOptionPane.showConfirmDialog(null, "¬øDesea Cerrar esta ventana?", "Warning", confirmacion);
            if (numeroConfirmacion == 0) {
                this.dispose();
            } else {
                //Validar que no exista y agregar   
                FacturaDao facturax = new FacturaDao();
                int idFactura = Integer.parseInt(this.txt_idFactura.getText());
                if (facturax.validarExistaFactura(idFactura)) {
                    //Rescatar datos
                    Factura factura = facturax.obtenerFacturaPorID(idFactura);
                    this.txt_factura1.setText(String.valueOf(factura.getFactura1()));
                    this.txt_factura2.setText(String.valueOf(factura.getFactura2()));
                    this.txt_factura3.setText(String.valueOf(factura.getFactura3()));
                    this.txt_factura4.setText(String.valueOf(factura.getFactura4()));
                    this.txt_factura5.setText(String.valueOf(factura.getFactura5()));
                    JOptionPane.showMessageDialog(null, "Datos cargados, ya existe este ID");
                } else {
                    //Agregar nuevo             
                    if (facturax.insertarFactura(0, 0, 0, 0, 0)) {
                        JOptionPane.showMessageDialog(null, "Agregado Correctamente");                        
                        vista.ordenTrabajo.menuOT.txt_facturasAsignadas.setText(this.txt_idFactura.getText());

                    } else {
                        JOptionPane.showMessageDialog(null, "No se pudo agregar");
                    }
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_btnAgregarYCerrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            //Validar si existe, si existe modifico
            FacturaDao facturax = new FacturaDao();
            int idFactura = Integer.parseInt(this.txt_idFactura.getText());
            if (facturax.validarExistaFactura(idFactura)) {
                //modificar datos
                if (this.txt_factura1.getText().isEmpty()) {
                    throw new Exception("Ingrese al menos una factura, sino utilizar opcion agregar y cerrar.");
                }
                int factura1 = Integer.parseInt(this.txt_factura1.getText());

                int factura2 = 0;
                if (!this.txt_factura2.getText().isEmpty()) {
                    factura2 = Integer.parseInt(this.txt_factura2.getText());
                }

                int factura3 = 0;
                if (!this.txt_factura3.getText().isEmpty()) {
                    factura3 = Integer.parseInt(this.txt_factura3.getText());
                }

                int factura4 = 0;
                if (!this.txt_factura4.getText().isEmpty()) {
                    factura4 = Integer.parseInt(this.txt_factura4.getText());
                }

                int factura5 = 0;
                if (!this.txt_factura2.getText().isEmpty()) {
                    factura5 = Integer.parseInt(this.txt_factura2.getText());
                }
                
                if (facturax.modificarFactura(factura1, factura2, factura3, factura4, factura5, idFactura)) {
                    JOptionPane.showMessageDialog(null, "Modificado Correctamente");
                }else{
                    JOptionPane.showMessageDialog(null, "No se pudo modificar");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

