    public void obtenerUltimoNroOT() {
        if (this.txt_nroOrdenTrabajo.getText().equals("00000")) {
            OrdenTrabajoDao ordenx = new OrdenTrabajoDao();
            int nroOT = ordenx.obtenerUltimoNroOrdenTrabajo() + 1;
            this.txt_nroOrdenTrabajo.setText(String.valueOf(nroOT));
        }
    }

    public void generarFechaHoy() {
        Calendar today = Calendar.getInstance();
        Date fechaHoy = today.getTime();
        SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
        String fechaStringBase = format.format(fechaHoy);
        //System.out.println("Fecha: " + fechaStringBase);
        this.txt_fechaCreacionHoy.setText(fechaStringBase);
    }

