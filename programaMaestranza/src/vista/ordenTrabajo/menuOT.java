    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            OrdenTrabajoDao otx = new OrdenTrabajoDao();
            //validar si existe
            int nroOrdenTrabajo = Integer.parseInt(vista.ordenTrabajo.menuOT.txt_nroOrdenTrabajo.getText());
            if (otx.validarSiExiste(nroOrdenTrabajo)) {
                //rescatar datos
                this.validarCamposVacios(vista.ordenTrabajo.menuOT.txt_nombreOT.getText(), "NOMBRE ORDEN TRABAJO");

                String nombreOT = vista.ordenTrabajo.menuOT.txt_nombreOT.getText();

                String nombreEmpresa = vista.ordenTrabajo.menuOT.txt_nombreCliente.getText();
                int idEmpresa = otx.obtenerIdEmpresa(nombreEmpresa.toUpperCase());

                String nombreResponsable = vista.ordenTrabajo.menuOT.txt_nombreResponsable.getText();

                int numeroOC = 0;
                if (!vista.ordenTrabajo.menuOT.txt_OrdenCompra.getText().isEmpty()) {
                    numeroOC = Integer.parseInt(vista.ordenTrabajo.menuOT.txt_OrdenCompra.getText());
                }

                String descripcion = "";
                if (!vista.ordenTrabajo.menuOT.txt_descripcion.getText().isEmpty()) {
                    descripcion = vista.ordenTrabajo.menuOT.txt_descripcion.getText();
                }

                String fechaTermino = "";
                if (!vista.ordenTrabajo.menuOT.txt_fechaTermino.getText().isEmpty()) {
                    fechaTermino = vista.ordenTrabajo.menuOT.txt_fechaTermino.getText();
                }

                //Modificar
                if (otx.modificarOrdenTrabajo(nroOrdenTrabajo, idEmpresa, nombreResponsable, fechaTermino, descripcion, numeroOC, nombreOT)) {
                    JOptionPane.showMessageDialog(null, "Modificado Correctamente");
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo modificar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El número de OT no existe");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            //
            OrdenTrabajoDao otx = new OrdenTrabajoDao();
            int nroOrdenTrabajo = Integer.parseInt(vista.ordenTrabajo.menuOT.txt_nroOrdenTrabajo.getText());
            if (otx.validarSiExiste(nroOrdenTrabajo)) {
                //Existe asi que elimino
                int confirmacion = JOptionPane.YES_NO_OPTION;
                int numeroConfirmacion = JOptionPane.showConfirmDialog(null, "¿Desea Eliminarlo Realmente?", "Warning", confirmacion);
                if (numeroConfirmacion == 0) {
                    //Metodo eliminar
                    if (otx.eliminarOrdenTrabajo(nroOrdenTrabajo)) {
                        JOptionPane.showMessageDialog(null, "Eliminado Corretamente");
                        this.limpiarCampos();
                    }else{
                        JOptionPane.showMessageDialog(null, "No se ha podido eliminar");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "No existe la orden de trabajo con ese ID");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

