    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaTipoTrabajo.getModel();
        int a = this.tablaTipoTrabajo.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void llenarComboBoxGiroProveedores() {
        try {
            DepartamentoDao departamentox = new DepartamentoDao();
            ArrayList<String> listado = departamentox.rescatarNombresDeptoComboBox();

            this.cboDepartamento.removeAllItems();

            for (int i = -1; i < listado.size(); i++) {
                if (i == -1) {
                    this.cboDepartamento.addItem("Seleccionar");
                } else {
                    this.cboDepartamento.addItem(listado.get(i));
                }
            }
            /*Fin llenado*/
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema CBO: " + e.getMessage());
        }
    }

    public void cargarTablaInicio() {
        try {
            TipoTrabajoDao tipox = new TipoTrabajoDao();

            DefaultTableModel modelo = (DefaultTableModel) this.tablaTipoTrabajo.getModel();
            ArrayList<TipoTrabajo> listado = tipox.obtenerListadoTipoTrabajo();

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listado.size(); i++) {
                fila[0] = listado.get(i).getIdTipoTrabajo();
                fila[1] = listado.get(i).getNombreDepto();
                fila[2] = listado.get(i).getTipoDepto();
                fila[3] = listado.get(i).getValorHora();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }

    
