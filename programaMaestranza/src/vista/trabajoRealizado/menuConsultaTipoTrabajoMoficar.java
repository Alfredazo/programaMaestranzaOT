    private void cboDepartamentoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboDepartamentoItemStateChanged
        try {
            if (evt.getItem().equals("Seleccionar")) {
                if (this.tablaTipoTrabajo.getRowCount() >= 0) {
                    this.eliminarContenidoTabla();
                }
                this.cargarTablaInicio();
            } else {
                String nombreDpto = evt.getItem().toString();
                if (this.tablaTipoTrabajo.getRowCount() >= 0) {
                    this.eliminarContenidoTabla();
                }
                this.cargarTablaInicio(nombreDpto);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_cboDepartamentoItemStateChanged

    private void tablaTipoTrabajoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaTipoTrabajoMouseClicked
        try{
            if (evt.getClickCount()==2) {
                int idTipoTrabajo = Integer.parseInt(this.tablaTipoTrabajo.getValueAt(this.tablaTipoTrabajo.getSelectedRow(), 0).toString());
                
                vista.trabajoRealizado.modificarITEMTrabajoRealizado.txt_tipoTrabajo.setText(String.valueOf(idTipoTrabajo));
                vista.trabajoRealizado.modificarITEMTrabajoRealizado.txt_horasTrabajadas.setText("0");
                this.dispose();
            }            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tablaTipoTrabajoMouseClicked

