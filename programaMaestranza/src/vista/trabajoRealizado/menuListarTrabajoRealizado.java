    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaTrabajosRealizados.getModel();
        int a = this.tablaTrabajosRealizados.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void cargarTablaInicio() {
        try {
            TrabajosRealizadosDao trabajoRealizadox = new TrabajosRealizadosDao();
            DefaultTableModel modelo = (DefaultTableModel) this.tablaTrabajosRealizados.getModel();
            ArrayList<TrabajosRealizados> listado = trabajoRealizadox.listadoTrabajosRealizados();

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listado.size(); i++) {
                fila[0] = listado.get(i).getIdTrabajosRealizados();
                fila[1] = listado.get(i).getNroOT();
                fila[2] = listado.get(i).getNombreOT();
                fila[3] = listado.get(i).getNombreEmpresa();
                fila[4] = listado.get(i).getSubTotal();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }

