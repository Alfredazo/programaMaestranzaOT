    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        try {
            if (this.tablaTrabajosRealizados.getSelectedRowCount() < 0) {
                throw new Exception("Seleccione una fila para utilizar esta funcion");
            }

            String nombreUsuario = System.getProperty("user.name");

            Calendar today = Calendar.getInstance();
            Date fechaHoy = today.getTime();
            SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
            String fechaStringBase = format.format(fechaHoy);

            String nuevaCarpeta = "FECHA-HOY-" + fechaStringBase;
            //Crear directorio y comprobar si existe
            File file = new File("C:/Users/" + nombreUsuario + "/Desktop/TRABAJOS/" + nuevaCarpeta);
            if (!file.exists()) {
                if (file.mkdir()) {
                    System.out.println("Directory is created!");
                }
            }

            InputStream ExcelFileToRead = new FileInputStream("C:/FORMATOS/FORMATOTRABAJOS.xls");
            // OPCPackage pkg = OPCPackage.open(new File("C:/Users/Alfredo/Desktop/PEDIDOS/FORMATO.xlsx"));
            HSSFWorkbook wb = new HSSFWorkbook(ExcelFileToRead);
            //XSSFWorkbook wb = new XSSFWorkbook(pkg);
            String nombreHoja = wb.getSheetName(0);
            //HSSFSheet hoja = wb.getSheet(nombreHoja);
            HSSFSheet hoja = wb.getSheetAt(0);
            System.out.println("HOJA EXCEL: " + hoja);

            int idTrabajoRealizado = Integer.parseInt(this.tablaTrabajosRealizados.getValueAt(this.tablaTrabajosRealizados.getSelectedRow(), 0).toString());
            int nroOT = Integer.parseInt(this.tablaTrabajosRealizados.getValueAt(this.tablaTrabajosRealizados.getSelectedRow(), 1).toString());

            TrabajosRealizadosDao trabajox = new TrabajosRealizadosDao();
            TrabajoRealizadoExportacion trabajito = trabajox.exportarObjetoTrabajoRealizado(nroOT);

            HSSFRow row = hoja.createRow(0);
            HSSFCell cell = row.createCell(1);
            //Nombre empresa
            cell.setCellValue(trabajito.getNombreCliente());
            //Nombre OT
            row = hoja.createRow(1);
            cell = row.createCell(1);
            cell.setCellValue(trabajito.getNombreOT());
            //Nombre Responsable
            row = hoja.createRow(2);
            cell = row.createCell(1);
            cell.setCellValue(trabajito.getResponsable());
            //Nro insumo
            row = hoja.createRow(3);
            cell = row.createCell(1);
            cell.setCellValue(trabajito.getFechaInicio());
            //TOTAL
            row = hoja.createRow(4);
            cell = row.createCell(1);
            cell.setCellValue(trabajito.getFechaTermino());
            //N° OT
            row = hoja.createRow(5);
            cell = row.createCell(1);
            cell.setCellValue(trabajito.getNroOT());

            //N° OT
            row = hoja.createRow(6);
            cell = row.createCell(1);
            cell.setCellValue(trabajito.getSubTotal());

            //Recorrer listado
            ArrayList<ListadoTrabajosRealizados> listadoRecorrer = trabajox.listadoITEMSTrabajosRealizadosExportar(idTrabajoRealizado);
            int filaInicial = 10;
            int columnaRecorre = 0;
            /*Seleccionar fila inicial*/
            for (int i = 0; i < listadoRecorrer.size(); i++) {
                row = hoja.createRow(filaInicial++);
                for (int j = 0; j <= 6; j++) {
                    cell = row.createCell(j);
                    if (j == 0) {
                        //Primera columna
                        cell.setCellValue(listadoRecorrer.get(i).getNombreTrabajo());
                    } else if (j == 1) {
                        cell.setCellValue(listadoRecorrer.get(i).getTipoTrabajo());
                    } else if (j == 2) {
                        cell.setCellValue(listadoRecorrer.get(i).getNombreDepto());
                    } else if (j == 3) {
                        cell.setCellValue(listadoRecorrer.get(i).getHorasTrabajadas());
                    } else if (j == 4) {
                        cell.setCellValue(listadoRecorrer.get(i).getValorHora());
                    } else if (j == 5) {
                        cell.setCellValue(listadoRecorrer.get(i).getSubTotal());
                    } else if (j == 6) {
                        cell.setCellValue(listadoRecorrer.get(i).getDescripcion());
                    }                    
                }
            }

            String rutaNueva = "C:/Users/" + nombreUsuario + "/Desktop/TRABAJOS/" + nuevaCarpeta + "/ID TRABAJO-REALIZADOS -" + idTrabajoRealizado + ".xls";
            FileOutputStream fileOut = new FileOutputStream(rutaNueva);
            wb.write(fileOut);
            fileOut.flush();
            fileOut.close();
            JOptionPane.showMessageDialog(null, "Exportado Correctamente\n" + rutaNueva);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnExportarActionPerformed

