    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaUsuarios.getModel();
        int a = this.tablaUsuarios.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }
    
    public void cargarTablaInicio() {
        try {
            UsuarioDao usuariox = new UsuarioDao();
            DefaultTableModel modelo = (DefaultTableModel) this.tablaUsuarios.getModel();
            ArrayList<Usuario> listadoUsuario = usuariox.cargarListaUsuarios();
            
            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listadoUsuario.size(); i++) {
                fila[0] = listadoUsuario.get(i).getIdUsuario();
                fila[1] = listadoUsuario.get(i).getNroTrabajador();
                fila[2] = listadoUsuario.get(i).getClave();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }
    
