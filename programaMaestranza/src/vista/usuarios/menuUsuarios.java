    private void btnCrearPerfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPerfilActionPerformed
        try {
            this.validarCampoVacio(vista.usuarios.menuUsuarios.txt_nroUsuario.getText(), "NUMERO TRABAJADOR");
            
            int nroTrabajador = Integer.parseInt(vista.usuarios.menuUsuarios.txt_nroUsuario.getText());
            String clave = "";
            if (this.txt_clave.getText().length() <= 0) {
                throw new Exception("Debe ingresar una clave");
            }
            this.validarCampoVacio(this.txt_claveValida.getText(), "VALIDAR CLAVE");
            
            if (this.txt_clave.getText().equals(this.txt_claveValida.getText())) {
                clave = this.txt_clave.getText();
                
                UsuarioDao usuariox = new UsuarioDao();
                
                if (!usuariox.validarSiExisteUsuarioTrabajador(nroTrabajador)) {
                    if (usuariox.insertarTipoTrabajo(nroTrabajador, clave)) {
                        JOptionPane.showMessageDialog(null, "Agregado Correctamente");
                        this.limpiarCampos();
                    } else {
                        JOptionPane.showMessageDialog(null, "No se pudo Agregar");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Ya existe un usuario creado para este trabajador");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Las claves no coinciden");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnCrearPerfilActionPerformed

    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        try {
            if (evt.getClickCount() == 2) {
                UsuarioDao usuariox = new UsuarioDao();
                int nroTrabajador = Integer.parseInt(this.tablaUsuarios.getValueAt(this.tablaUsuarios.getSelectedRow(), 1).toString());
                int idUsuario = Integer.parseInt(this.tablaUsuarios.getValueAt(this.tablaUsuarios.getSelectedRow(), 0).toString());
                String clave = this.tablaUsuarios.getValueAt(this.tablaUsuarios.getSelectedRow(), 2).toString();
                String nombreUsuario = usuariox.obtenerNombreUsuario(nroTrabajador);
                
                this.txt_idUsuario.setText(String.valueOf(idUsuario));
                vista.usuarios.menuUsuarios.txtNombreUsuario.setText(nombreUsuario);
                vista.usuarios.menuUsuarios.txt_nroUsuario.setText(String.valueOf(nroTrabajador));
                this.txt_clave.setText(clave);
                this.txt_claveValida.setText(clave);
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        try {
            UsuarioDao usuariox = new UsuarioDao();
            int idUsuario = Integer.parseInt(this.txt_idUsuario.getText());
            if (usuariox.validarSiExisteIDUsuario(idUsuario)) {
                //Modificar
                int nroTrabajador = Integer.parseInt(vista.usuarios.menuUsuarios.txt_nroUsuario.getText());
                String clave = "";
                if (this.txt_clave.getText().length() <= 0) {
                    throw new Exception("Debe ingresar una clave");
                }
                this.validarCampoVacio(this.txt_claveValida.getText(), "VALIDAR CLAVE");
                
                if (this.txt_clave.getText().equals(this.txt_claveValida.getText())) {
                    clave = this.txt_clave.getText();
                    if (usuariox.modificarUsuario(nroTrabajador, clave, idUsuario)) {
                        JOptionPane.showMessageDialog(null, "Se agrego correctamente");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se pudo agregar");
                    }                    
                }else{
                    JOptionPane.showMessageDialog(null, "Claves no coinciden");
                }
            } else {
                JOptionPane.showMessageDialog(null, "No existe este ID usuario");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

