    private void txt_nombreBuscarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_nombreBuscarCaretUpdate
        try {
            String nombreBuscar;
            if (!this.txt_nombreBuscar.getText().trim().isEmpty()) {
                nombreBuscar = this.txt_nombreBuscar.getText().trim().toUpperCase();
                this.eliminarContenidoTabla();
                this.cargarTablaInicio(nombreBuscar);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_txt_nombreBuscarCaretUpdate

    private void tablaPersonasUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPersonasUsuariosMouseClicked
        if (evt.getClickCount() == 2) {
            String nombre = this.tablaPersonasUsuarios.getValueAt(this.tablaPersonasUsuarios.getSelectedRow(), 0).toString();
            String numero = this.tablaPersonasUsuarios.getValueAt(this.tablaPersonasUsuarios.getSelectedRow(), 1).toString();
            vista.usuarios.menuUsuarios.txtNombreUsuario.setText(nombre);
            vista.usuarios.menuUsuarios.txt_nroUsuario.setText(numero);
            this.dispose();
        }
    }//GEN-LAST:event_tablaPersonasUsuariosMouseClicked

