    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaPersonasUsuarios.getModel();
        int a = this.tablaPersonasUsuarios.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void cargarTablaInicio() {
        try {
            UsuarioDao usuariox = new UsuarioDao();
            DefaultTableModel modelo = (DefaultTableModel) this.tablaPersonasUsuarios.getModel();
            ArrayList<Persona> listaProveedores = usuariox.cargarNombreNroTrabajador();

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listaProveedores.size(); i++) {
                fila[0] = listaProveedores.get(i).getNombre();
                fila[1] = listaProveedores.get(i).getNroTrabajador();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }

    public void cargarTablaInicio(String nombre) {
        try {
            UsuarioDao usuariox = new UsuarioDao();
            DefaultTableModel modelo = (DefaultTableModel) this.tablaPersonasUsuarios.getModel();
            ArrayList<Persona> listaProveedores = usuariox.cargarNombreNroTrabajador(nombre);

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listaProveedores.size(); i++) {
                fila[0] = listaProveedores.get(i).getNombre();
                fila[1] = listaProveedores.get(i).getNroTrabajador();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema Tabla Proveedores: " + e.getMessage());
        }
    }

