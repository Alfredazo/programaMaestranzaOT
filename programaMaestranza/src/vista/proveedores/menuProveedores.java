    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        try {
            this.comprobarSiVieneVacio(this.txt_rutEmpresa.getText(), "RUT EMPRESA");

            String rut = vista.proveedores.menuProveedores.txt_rutEmpresa.getText().replace(".", "").toUpperCase();

            ProveedorDao provedorDao = new ProveedorDao();
            int confirmacion = JOptionPane.YES_NO_OPTION;
            //System.out.println("Numero Confirmacion ELiminacion: "+ confirmacion);
            int numeroConfirmacion = JOptionPane.showConfirmDialog(null, "¿Desea Eliminarlo Realmente?", "Warning", confirmacion);

            if (numeroConfirmacion == 0) {
                if (provedorDao.eliminarProveedor(rut)) {
                    JOptionPane.showMessageDialog(null, "Eliminado Correctamente");
                    this.limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo Eliminar");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_btn_eliminarActionPerformed

