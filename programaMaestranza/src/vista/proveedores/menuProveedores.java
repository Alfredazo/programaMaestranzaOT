    private void txt_rutEmpresaCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_rutEmpresaCaretUpdate
        //Validar si existe rut en la bd   
        try {
            ProveedorDao provedorDao = new ProveedorDao();
            if (this.txt_rutEmpresa.getText().trim().length() == 0) {
                //No hacer metodo
            } else {
                //Buscar si existe carga datos, sino existe no hacer nada
                if (provedorDao.validarSiExisteProveedor(this.txt_rutEmpresa.getText())) {
                    Proveedor proveedor = new Proveedor();
                    proveedor = provedorDao.rescatarProveedorSegunRut(this.txt_rutEmpresa.getText().trim().toUpperCase());
                    this.txt_giro.setText(proveedor.getGiro());
                    this.txt_nombreEmpresa.setText(proveedor.getNombreEmpresa());
                    this.txt_paginaWeb.setText(proveedor.getPaginaWeb());
                    this.txt_nombreContacto.setText(proveedor.getNombreContacto());
                    this.txt_numeroContacto.setText(proveedor.getTelefono());
                    this.txt_correoContacto.setText(proveedor.getCorreo());
                    this.txt_nombreBanco.setText(proveedor.getNombreBanco());
                    this.txt_nroCuentaCorriente.setText(proveedor.getNroCuentaCorriente());
                } else {
                    System.out.println("No encontrado");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_txt_rutEmpresaCaretUpdate

