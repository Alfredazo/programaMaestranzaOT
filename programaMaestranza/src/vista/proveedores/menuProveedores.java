            //Rescatar datos
            String nombreEmpresa = this.txt_nombreEmpresa.getText();
            String giro = this.txt_giro.getText();
            String rutEmpresa = this.txt_rutEmpresa.getText().trim().replace(".", "").toUpperCase();

            String paginaWeb = this.txt_paginaWeb.getText();
            if (paginaWeb.isEmpty()) {
                paginaWeb = "";
            }

            String nombreContacto = this.txt_nombreContacto.getText();
            if (nombreContacto.isEmpty()) {
                nombreContacto = "";
            }

            String numeroContacto = this.txt_numeroContacto.getText();
            if (numeroContacto.isEmpty()) {
                nombreContacto = "";
            }

            String correoContacto = this.txt_correoContacto.getText();
            if (correoContacto.isEmpty()) {
                correoContacto = "";
            }

            String nombreBanco = this.txt_nombreBanco.getText();
            if (nombreBanco.isEmpty()) {
                nombreBanco = "";
            }
            
            String nroCuentaCorriente = this.txt_nroCuentaCorriente.getText();
            if (nroCuentaCorriente.isEmpty()) {
                nroCuentaCorriente = "";
            }
            

            /*Generar Proveedor*/
            ProveedorDao provedorDao = new ProveedorDao();
            //Agregar datos del menu OJO MAÑANA 
            if (provedorDao.insertarProveedor(nombreEmpresa, giro, rutEmpresa, paginaWeb, nombreContacto, numeroContacto, correoContacto,nombreBanco,nroCuentaCorriente)) {
                JOptionPane.showMessageDialog(null, "Proveedor Ingresado Correctamente");
                this.limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo ingresar");
            }

