    /*Metodos Listar Trabajador*/
    public void eliminarContenidoTabla() {
        DefaultTableModel tb = (DefaultTableModel) this.tablaTrabajadores.getModel();
        int a = this.tablaTrabajadores.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

    public void cargarTablaListadoCompleto() {
        //Listar empresa
        try {
            //Vaciar Campos de la tabla
            eliminarContenidoTabla();
            PersonaDao personaDao = new PersonaDao();
            /*cambiar datos desde el nuevo cbo*/

            DefaultTableModel modelo = (DefaultTableModel) this.tablaTrabajadores.getModel();
            ArrayList<Persona> listaTrabajadores = personaDao.lisdoTrabajadores();

            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listaTrabajadores.size(); i++) {
                fila[0] = listaTrabajadores.get(i).getRut();
                fila[1] = listaTrabajadores.get(i).getNombre();
                fila[2] = listaTrabajadores.get(i).getApellido();
                fila[3] = listaTrabajadores.get(i).getCargo();
                fila[4] = listaTrabajadores.get(i).getNroTrabajador();
                fila[5] = listaTrabajadores.get(i).getNumeroEmergencia1();
                fila[6] = listaTrabajadores.get(i).getNumeroEmergencia2();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    

    public void cargarTablaListadoPorFiltroLetras(String nombreBuscar) {
        //Listar empresa
        try {
            //Vaciar Campos de la tabla
            eliminarContenidoTabla();
            PersonaDao personaDao = new PersonaDao();
            /*cambiar datos desde el nuevo cbo*/

            DefaultTableModel modelo = (DefaultTableModel) this.tablaTrabajadores.getModel();
            ArrayList<Persona> listaTrabajadores = personaDao.lisdoTrabajadoresFiltrado(nombreBuscar);
            Object[] fila = new Object[modelo.getColumnCount()];
            for (int i = 0; i < listaTrabajadores.size(); i++) {
                fila[0] = listaTrabajadores.get(i).getRut();
                fila[1] = listaTrabajadores.get(i).getNombre();
                fila[2] = listaTrabajadores.get(i).getApellido();
                fila[3] = listaTrabajadores.get(i).getCargo();
                fila[4] = listaTrabajadores.get(i).getNroTrabajador();
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void comprobarSiVieneVacio(String dato, String campo) throws Exception {
        if (dato.trim().length() <= 0) {
            throw new Exception("El campo " + campo + " no puede venir en blanco.");
        }
    }

    private void btn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_grabarActionPerformed
        //Creacion Persona
        try {
            String rut, nombre, apellido, cargo;
            int nroTrabajador;

            //Validacion vacio
            this.comprobarSiVieneVacio(this.txt_rut.getText(), "RUT");
            this.comprobarSiVieneVacio(this.txt_nombre.getText(), "NOMBRE");
            this.comprobarSiVieneVacio(this.txt_apellido.getText(), "APELLIDO");
            this.comprobarSiVieneVacio(this.txt_cargo.getText(), "CARGO");

            rut = this.txt_rut.getText().replace(".", "");
            nombre = this.txt_nombre.getText();
            apellido = this.txt_apellido.getText();
            cargo = this.txt_cargo.getText();
            nroTrabajador = Integer.parseInt(this.nroTrabajador.getValue().toString());
            
            String numeroEmergencia1 = "";
            if (!this.txt_nroEmergencia1.getText().isEmpty()) {
                numeroEmergencia1 =  this.txt_nroEmergencia1.getText();
            }
            
            String numeroEmergencia2 = "";
            if (!this.txt_nroEmergencia2.getText().isEmpty()) {
                numeroEmergencia2 =  this.txt_nroEmergencia2.getText();
            }

            PersonaDao persona = new PersonaDao();
            boolean agregado = persona.insertarTrabajador(rut, nombre, apellido, cargo, nroTrabajador,numeroEmergencia1,numeroEmergencia2);
            if (agregado) {
                JOptionPane.showMessageDialog(null, "Se Agrego Correctamente");
                this.cargarTablaListadoCompleto();
            } else {
                JOptionPane.showMessageDialog(null, "NO se pudo Agregar");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage());

        }
    }//GEN-LAST:event_btn_grabarActionPerformed

   

